name = 'yooshink'
print(type('yooshink'))
print(name)

# Result = <class 'list'>
print("Names - ")
names = ['yooshin','TEST',"TEST2"]
print(type(['yooshin','TEST',"TEST2"]))
print(type(names))
print(names)
print(names[0])
print(names[1])
print(names[2])

